const EXCLUDE_PHOTO_IF_KEYWORDS = ["??", "????", "??", "???", "19.5%", "23.5%", "???"];

function scorePhotoCase(question, entry) {
  let keywordHits = 0;
  entry.keywords.forEach((kw) => {
    if (question.text.includes(kw)) keywordHits += 1;
  });
  if (keywordHits === 0) return 0;
  let score = keywordHits * 2;
  if (entry.topics.includes(question.topic)) score += 1;
  return score;
}

export function pickPhotoCasesForQuestion(question, limit = 2) {
  if (EXCLUDE_PHOTO_IF_KEYWORDS.some((kw) => question.text.includes(kw))) {
    return [];
  }

  const ranked = PHOTO_CASE_LIBRARY.map((entry) => ({
    entry,
    score: scorePhotoCase(question, entry),
  }))
    .filter((item) => item.score >= 2)
    .sort((a, b) => b.score - a.score);

  if (!ranked.length) return [];

  return ranked.slice(0, limit).map(({ entry }) => ({
    id: entry.id,
    text: entry.text,
    image: entry.image,
    credit: entry.credit,
    regulation: entry.regulation,
    hotspots: entry.hotspots,
  }));
}

