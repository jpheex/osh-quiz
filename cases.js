function toCaseObjects(texts, question) {
  return texts.map((text) => buildCase(text, question));
}

function mergePhotoAndTextCases(question) {
  const photoCases = pickPhotoCasesForQuestion(question, 2);
  if (!photoCases.length) return null;

  const kwHit = KEYWORD_CASES.find((item) => question.text.includes(item.kw));
  const texts = kwHit?.cases || TOPIC_CASES[question.topic] || [];
  const textCases = texts.slice(0, 1).map((text) => buildCase(text, question));

  return [...photoCases, ...textCases].slice(0, 3);
}

export function resolveCases(question) {
  if (question.cases?.length) {
    return question.cases.map((item) =>
      typeof item === "string" ? buildCase(item, question) : item
    );
  }

  const merged = mergePhotoAndTextCases(question);
  if (merged?.length) return merged;

  const kwHit = KEYWORD_CASES.find((item) => question.text.includes(item.kw));
  if (kwHit) return toCaseObjects(kwHit.cases, question);
  const texts = TOPIC_CASES[question.topic] || [
    "職業災害多源於管理疏失與現場條件未改善，落實法規可有效預防。",
    "勞動檢查常針對本題相關項目開立缺失，實務上應列為重點稽核。",
  ];
  return toCaseObjects(texts, question);
}
